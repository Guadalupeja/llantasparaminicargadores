<script>
    function ruguexFloatingChat(products, submitUrl) {
        return {
            products,
            submitUrl,
            isOpen: false,
            showPreview: true,
            messages: [],
            isTyping: false,
            selectedType: null,
            selectedMeasure: null,
            showLeadForm: false,
            leadMode: null,
            isSubmitting: false,
            submitErrorMessage: '',
            isBusinessHours: false,
            form: {
                nombre: '',
                empresa: '',
                telefono: '',
                correo: '',
                mensaje: '',
            },

            init() {
                this.isBusinessHours = this.isWithinBusinessHours();
                this.startConversation();
            },

            openChat() {
                this.isOpen = true;
                this.showPreview = false;
                this.$nextTick(() => this.scrollToBottom());
            },

            closeChat() {
                this.isOpen = false;
                this.showPreview = true;
            },

            startConversation() {
                this.messages = [];
                this.selectedType = null;
                this.selectedMeasure = null;
                this.showLeadForm = false;
                this.leadMode = null;
                this.submitErrorMessage = '';
                this.form = {
                    nombre: '',
                    empresa: '',
                    telefono: '',
                    correo: '',
                    mensaje: '',
                };

                this.addBotMessage('Hola, soy tu asesor virtual de Ruguex. Te ayudaré a encontrar la llanta adecuada para tu minicargador.');
                this.addBotOptions('¿Qué tipo de llanta estás buscando?', [
                    { label: 'Sólida', value: 'solida', action: 'type' },
                    { label: 'Neumática', value: 'neumatica', action: 'type' },
                ]);
            },

            async handleOption(option) {
                this.disableCurrentOptions();
                this.addUserMessage(option.label);

                if (option.action === 'type') {
                    this.selectedType = option.value;

                    await this.botReply(() => {
                        this.addBotOptions('Perfecto. Ahora dime qué medida necesitas.', [
                            { label: '31X10-20/7.5 (10-16.5)', value: '10-16.5', action: 'measure' },
                            { label: '33X12-20/7.5 (12-16.5)', value: '12-16.5', action: 'measure' },
                        ]);
                    });

                    return;
                }

                if (option.action === 'measure') {
                    this.selectedMeasure = option.value;
                    const currentProducts = this.products?.[this.selectedType]?.[this.selectedMeasure] ?? [];

                    let productBlockId = null;

                    await this.botReply(() => {
                        this.addBotMessage('Estas opciones están disponibles para compra en línea:');
                        productBlockId = this.addBotProducts(currentProducts);
                    }, 'none');

                    this.scrollToProductBlock(productBlockId);

                    setTimeout(() => {
                        this.addBotOptions(
                            'Si lo prefieres, también puedo ayudarte a solicitar una cotización o hablar con un especialista.',
                            this.getFinalActions()
                        );
                    }, 250);

                    return;
                }

                if (option.action === 'final_action') {
                    if (option.value === 'whatsapp') {
                        window.dataLayer = window.dataLayer || [];
                        window.dataLayer.push({
                            event: 'chat_whatsapp_click',
                            tipo_llanta: this.selectedTypeLabelForForm(),
                            medida: this.selectedMeasureLabelForForm(),
                            source: 'chat_ruguex',
                        });

                        window.open(
                            'https://wa.me/528332395885?text=Hola,%20necesito%20ayuda%20para%20elegir%20una%20llanta%20para%20minicargador.',
                            '_blank'
                        );
                        return;
                    }

                    if (option.value === 'cotizacion' || option.value === 'especialista') {
                        this.leadMode = option.value;

                        await this.botReply(() => {
                            this.addBotMessage(
                                option.value === 'cotizacion'
                                    ? 'Con gusto. Compárteme tus datos para solicitar tu cotización y un especialista te contactará en breve.'
                                    : 'Con gusto. Compárteme tus datos y un especialista te contactará en breve.'
                            );
                            this.showLeadForm = true;
                        });

                        return;
                    }

                    if (option.value === 'restart') {
                        this.startConversation();
                    }
                }
            },

            getFinalActions() {
                const actions = [];

                if (this.isBusinessHours) {
                    actions.push({
                        label: 'Hablar por WhatsApp',
                        value: 'whatsapp',
                        action: 'final_action',
                    });
                }

                actions.push(
                    {
                        label: 'Solicitar cotización',
                        value: 'cotizacion',
                        action: 'final_action',
                    },
                    {
                        label: 'Hablar con un especialista',
                        value: 'especialista',
                        action: 'final_action',
                    },
                    {
                        label: 'Reiniciar',
                        value: 'restart',
                        action: 'final_action',
                    }
                );

                return actions;
            },

            addBotMessage(text) {
                this.messages.push({
                    type: 'bot',
                    kind: 'text',
                    text,
                });
                this.scrollToBottom();
            },

            addBotOptions(text, options) {
                this.messages.push({
                    type: 'bot',
                    kind: 'options',
                    text,
                    options: options.map(option => ({
                        ...option,
                        disabled: false,
                    })),
                });
                this.scrollToBottom();
            },

            addBotProducts(products) {
                const productBlockId = 'products-' + Date.now();

                this.messages.push({
                    type: 'bot',
                    kind: 'products',
                    text: 'Da clic en la opción que deseas revisar.',
                    products,
                    blockId: productBlockId,
                });

                return productBlockId;
            },

            addUserMessage(text) {
                this.messages.push({
                    type: 'user',
                    kind: 'text',
                    text,
                });
                this.scrollToBottom();
            },

            disableCurrentOptions() {
                const lastMessage = this.messages[this.messages.length - 1];

                if (lastMessage && lastMessage.kind === 'options') {
                    lastMessage.options = lastMessage.options.map(option => ({
                        ...option,
                        disabled: true,
                    }));
                }
            },

            async botReply(callback, scrollMode = 'bottom') {
                this.isTyping = true;
                this.scrollToBottom();

                await new Promise(resolve => setTimeout(resolve, 700));

                this.isTyping = false;
                callback();

                this.$nextTick(() => {
                    if (scrollMode === 'bottom') {
                        this.scrollToBottom();
                    }
                });
            },

            selectedTypeLabelForForm() {
                return this.selectedType === 'solida' ? 'Sólida' : 'Neumática';
            },

            selectedMeasureLabelForForm() {
                if (this.selectedMeasure === '10-16.5') {
                    return '31X10-20/7.5 (10-16.5)';
                }

                if (this.selectedMeasure === '12-16.5') {
                    return '33X12-20/7.5 (12-16.5)';
                }

                return '';
            },

            async submitLeadForm() {
                this.submitErrorMessage = '';
                this.isSubmitting = true;

                try {
                    const response = await fetch(this.submitUrl, {
                        method: 'POST',
                        headers: {
                            'Content-Type': 'application/json',
                            'X-CSRF-TOKEN': document.querySelector('meta[name="csrf-token"]')?.getAttribute('content'),
                            'Accept': 'application/json',
                        },
                        body: JSON.stringify({
                            nombre: this.form.nombre,
                            empresa: this.form.empresa,
                            tipo_llanta: this.selectedTypeLabelForForm(),
                            medida: this.selectedMeasureLabelForForm(),
                            telefono: this.form.telefono,
                            correo: this.form.correo,
                            mensaje: this.form.mensaje,
                            origen: this.leadMode === 'cotizacion'
                                ? 'Chat minicargadores - Solicitar cotización'
                                : 'Chat minicargadores - Hablar con un especialista',
                        }),
                    });

                    const data = await response.json();

                    if (!response.ok) {
                        throw new Error(data.message || 'No fue posible enviar tu solicitud.');
                    }

                    this.showLeadForm = false;

                    window.dataLayer = window.dataLayer || [];
                    window.dataLayer.push({
                        event: 'chat_lead_submit',
                        lead_type: this.leadMode === 'cotizacion' ? 'cotizacion' : 'especialista',
                        tipo_llanta: this.selectedTypeLabelForForm(),
                        medida: this.selectedMeasureLabelForForm(),
                        source: 'chat_ruguex',
                    });

                    await this.botReply(() => {
                        this.addBotMessage('Gracias. En breve será contactado por el departamento de ventas.');
                        this.addBotOptions('¿Deseas hacer otra consulta?', [
                            { label: 'Reiniciar', value: 'restart', action: 'final_action' },
                        ]);
                    });

                    this.form = {
                        nombre: '',
                        empresa: '',
                        telefono: '',
                        correo: '',
                        mensaje: '',
                    };
                } catch (error) {
                    this.submitErrorMessage = error.message || 'Ocurrió un error al enviar la solicitud.';
                } finally {
                    this.isSubmitting = false;
                }
            },

            cancelLeadForm() {
                this.showLeadForm = false;
                this.leadMode = null;
                this.submitErrorMessage = '';
            },

            isWithinBusinessHours() {
                const now = new Date();

                const formatter = new Intl.DateTimeFormat('en-US', {
                    timeZone: 'America/Mexico_City',
                    weekday: 'short',
                    hour: '2-digit',
                    minute: '2-digit',
                    hour12: false,
                });

                const parts = formatter.formatToParts(now);
                const weekday = parts.find(part => part.type === 'weekday')?.value;
                const hour = parseInt(parts.find(part => part.type === 'hour')?.value ?? '0', 10);
                const minute = parseInt(parts.find(part => part.type === 'minute')?.value ?? '0', 10);

                const weekdays = ['Mon', 'Tue', 'Wed', 'Thu', 'Fri'];
                const currentMinutes = (hour * 60) + minute;
                const startMinutes = 9 * 60;
                const endMinutes = 18 * 60;

                return weekdays.includes(weekday) && currentMinutes >= startMinutes && currentMinutes < endMinutes;
            },

            scrollToBottom() {
                this.$nextTick(() => {
                    const container = this.$refs.messagesContainer;
                    if (container) {
                        container.scrollTop = container.scrollHeight;
                    }
                });
            },

            scrollToProductBlock(blockId) {
                this.$nextTick(() => {
                    const container = this.$refs.messagesContainer;
                    const element = container?.querySelector(`[data-product-block="${blockId}"]`);

                    if (container && element) {
                        const containerRect = container.getBoundingClientRect();
                        const elementRect = element.getBoundingClientRect();
                        const relativeTop = elementRect.top - containerRect.top + container.scrollTop;

                        container.scrollTo({
                            top: Math.max(relativeTop - 8, 0),
                            behavior: 'smooth',
                        });
                    }
                });
            },

            trackProductClick(product) {
